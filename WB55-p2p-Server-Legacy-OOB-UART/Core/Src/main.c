extern USBD_HandleTypeDef hUsbDeviceFS;

subKeyBoard keyBoardHIDsub = {0,0,0,0,0,0,0,0};

// Tablica przycisków
Button_t buttons[NUM_BUTTONS] = {
    {GPIOA, GPIO_PIN_0, 0x04, false, 0},  // 'A'
    {GPIOA, GPIO_PIN_1, 0x05, false, 0},  // 'B'
};

